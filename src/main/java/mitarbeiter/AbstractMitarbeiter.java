package mitarbeiter;

abstract class AbstractMitarbeiter implements Mitarbeiter{

    private Long id;

    private String name;

    public AbstractMitarbeiter(Long id, String name) {
        this.id = id;
        this.name = name;
    }

    @Override
    public Long getId() {
        return id;
    }

    @Override
    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }


}
